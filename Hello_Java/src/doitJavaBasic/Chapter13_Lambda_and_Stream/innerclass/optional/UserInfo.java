package Chapter13_Lambda_and_Stream.innerclass.optional;

public class UserInfo {
    private int ID;
    private String name;

    public UserInfo(int ID, String name) {
        this.ID = ID;
        this.name = name;
    }

    public int getID() {
        return ID;
    }

    public String getName() {
        return name;
    }
}
